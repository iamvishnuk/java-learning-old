
public class Maths {
	
	float result;
	
	void addition(int num1,int num2) {
		result=num1+num2;
		System.out.println("Result ="+result);
	}
	
	void subtraction(int num1, int num2) {
		result=num1-num2;
		System.out.println("Result ="+result);
	}
	
	void multiplication(int num1, int num2) {
		result=num1*num2;
		System.out.println("Result ="+result);
	}
	
	void division(int num1, int num2) {
		result=num1/num2;
		System.out.println("Result ="+result);
	}

}
