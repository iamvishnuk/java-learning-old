
public class Calcuations {
	
	int sum;
	
	void addition(int num1, int num2) {
		sum=num1+num2;
		System.out.println("Result :"+sum);
	}
	
	void substraction(int num1, int num2) {
		sum=num1-num2;
		System.out.println("Result :"+sum);
	}
	
	void multiplication(int num1, int num2) {
		sum=num1*num2;
		System.out.println("Result :"+sum);
	}
	
	void division(int num1, int num2) {
		sum=num1/num2;
		System.out.println("Result :"+sum);
	}

}
